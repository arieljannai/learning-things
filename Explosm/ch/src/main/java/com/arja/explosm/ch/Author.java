public class Author {

    private String name;
    private String email;
    private String facebook;
    private String twitter;
    private String imageUrl;

    public Author(String name, String email, String facebook, String twitter, String image_url) {
        this.name = name;
        this.email = email;
        this.facebook = facebook;
        this.twitter = twitter;
        this.imageUrl = image_url;
    }
}
